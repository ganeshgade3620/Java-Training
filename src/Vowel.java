public class Vowel {

    //write a program to check the given char is vowel or not
    public static void main(String[] args) {
        char a = 'a';
        if (a == 'a' || a == 'e' || a == 'i' || a == 'o' || a == 'v') {
            System.out.println("vowel");
        } else {
            System.out.println("not vowel");
        }
    }
}
